export function getStudentsByClass(className: string): Student[] {
    return MOCK_STUDENTS.filter(s => s.class === className);
}

export function getStudentById(id: string): Student | undefined {
    return MOCK_STUDENTS.find(s => s.id === id);
}

export function searchStudents(query: string): Student[] {
    const lowerQuery = query.toLowerCase();
    return MOCK_STUDENTS.filter(s =>
        s.name.toLowerCase().includes(lowerQuery) ||
        s.nis.includes(query) ||
        s.nisn.includes(query) ||
        s.class.toLowerCase().includes(lowerQuery)
    );
}
