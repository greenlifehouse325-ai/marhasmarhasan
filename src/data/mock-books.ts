export function getBookById(id: string): Book | undefined {
    return MOCK_BOOKS.find(b => b.id === id);
}

export function searchBooks(query: string): Book[] {
    const lowerQuery = query.toLowerCase();
    return MOCK_BOOKS.filter(b =>
        b.title.toLowerCase().includes(lowerQuery) ||
        b.author.toLowerCase().includes(lowerQuery) ||
        b.isbn.includes(query) ||
        b.category.toLowerCase().includes(lowerQuery)
    );
}

export function getActiveLendings(): Lending[] {
    return MOCK_LENDINGS.filter(l => l.status === 'active' || l.status === 'overdue');
}

export function getOverdueLendings(): Lending[] {
    return MOCK_LENDINGS.filter(l => l.status === 'overdue');
}

export function getPendingFines(): Fine[] {
    return MOCK_FINES.filter(f => f.status === 'pending');
}
